#user_input
first_num = int(input("Enter the first number: "))
second_num = int(input("Enter the second number: "))

#Equal
if first_num == second_num:
    print(f"{first_num} equal to {second_num}: True")
else:
    print(f"{first_num} equal to {second_num}: False")
#Greater than
if first_num > second_num:
    print(f"{first_num} greater than {second_num}: True")
else:
    print(f"{first_num} greater than {second_num}: False")
#less than
if first_num < second_num:
    print(f"{first_num} less than {second_num}: True")
else:
    print(f"{first_num} less than {second_num}: False")
#NOT equal to
if first_num != second_num:
    print(f"{first_num} not equal to {second_num}: True")
else:
    print(f"{first_num} not equal to {second_num}: False")
#less than or equal to 
if first_num >= second_num:
    print(f"{first_num} greater than or equal to {second_num}: True")
else:
    print(f"{first_num} greater than or equal to {second_num}: False")
#less than or equal to
if first_num <= second_num:
    print(f"{first_num} less than or equal to {second_num}: True")
else:
    print(f"{first_num} less than or equal to {second_num}: False")